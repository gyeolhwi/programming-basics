package j01_출력;
public class Myinfo {

	public static void main(String[] args) {
		String name; 				// 변수선언 (메모리 할당 됨) and 대입
		name = "김결휘";			// 변수에 값 할당
		System.out.println(name);	// 출력
		
		//String name; 변수 선언은 번복이 되지않음
		name = "gyeori";
		System.out.println(name);
		
		int age;
		age = 1;
		System.out.println(age);
		// 자동완성 단축키 ⌘ ⏎
		// 실행 단축키 ⌘ ⇧ f11
		
		// String 클래스자료형
		
	}
}